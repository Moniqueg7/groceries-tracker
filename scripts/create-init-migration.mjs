/**
 * Creates prisma/migrations/<name>/migration.sql from current schema (UTF-8, no BOM).
 */
import { execSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const migrationName = process.argv[2] ?? "20260527140000_init";
const dir = resolve("prisma/migrations", migrationName);
mkdirSync(dir, { recursive: true });

const sql = execSync(
  "npx prisma migrate diff --from-empty --to-schema-datamodel prisma/schema.prisma --script",
  { encoding: "utf8" }
);
const clean = sql.replace(/^\uFEFF/, "").trimStart();
writeFileSync(resolve(dir, "migration.sql"), `${clean}\n`, { encoding: "utf8" });

writeFileSync(
  resolve("prisma/migrations/migration_lock.toml"),
  [
    "# Please do not edit this file manually",
    "# It should be added in your version-control system (i.e. Git)",
    'provider = "postgresql"',
    "",
  ].join("\n"),
  { encoding: "utf8" }
);

const buf = readFileSync(resolve(dir, "migration.sql"));
const hasBom = buf[0] === 0xef && buf[1] === 0xbb && buf[2] === 0xbf;
console.log(`Created ${migrationName} (${buf.length} bytes, BOM=${hasBom})`);
